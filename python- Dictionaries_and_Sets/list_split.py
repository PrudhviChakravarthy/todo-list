# Example string
sentence = "This is a sample sentence."

# Split the string by spaces (default delimiter)
words = sentence.split()

print(words)  # Output: ["This", "is", "a", "sample", "sentence."]

# Split the string by a custom delimiter (e.g., comma)
csv_data = "Alice,Bob,Charlie,David"
names = csv_data.split(",")

print(names)  # Output: ["Alice", "Bob", "Charlie", "David"]
